public class Main {
    public static void main(String[] args)
    {
        int box = -159;
        long boxLong = 341414134;
        short boxShort = 27897;
        byte boxByte = 67;
        float x = 27.12f;
        double y = 2.78641351235;
        System.out.println("Значение переменной box с типом int равно " + box);
        System.out.println("Значение переменной boxLong с типом long равно " + boxLong);
        System.out.println("Значение переменной boxShort с типом short равно " + boxShort);
        System.out.println("Значение переменной boxByte с типом byte равно " + boxByte);
        System.out.println("Значение переменной x с типом float равно " + x);
        System.out.println("Значение переменной y с типом double равно " + y);
    }
}